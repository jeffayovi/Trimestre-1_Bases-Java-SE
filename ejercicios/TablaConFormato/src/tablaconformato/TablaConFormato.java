
package tablaconformato;
/*
Programa que imprima una tabla con saltos de línea (\n) y tabulaciones (\t).
*/

public class TablaConFormato {

    
    public static void main(String[] args) {
        System.out.println("Nombre\t\tEdad\t\tCuidad\t\tPuesto");
        System.out.println("---------------------------------------------------------------");
        System.out.println("Ana Garcia\t 25\t\tMadrid\t\tDesarrolladora");
        System.out.println("Luis Perez\t 30\t\tBarcelona\tDiseñador");
        System.out.println("Carlos Ruiz\t 35\t\tSevilla\t\tAnalista");
        System.out.println("Martha Lopez\t 28\t\tValencia\tGerente");
        
    }
    
}
